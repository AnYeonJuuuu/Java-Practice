package str;

public class Main {

	public static void main(String[] args) {

		// 문자열(== 문자 배열)
		
		String name = "An Yeon Ju";
		
		//인덱스 이용해서 문자 한 개만 가져올 수 있음!!
		char x = name.charAt(3); // 3번 인덱스 글자를 가져오겠다 할 때! >> 3번째 글자가 아님 주의!!
		
		System.out.println(x);
		
		//특정 인덱스 값이 공백일 때 줄 바꿈 해주면 됨!!
		
	}

}
